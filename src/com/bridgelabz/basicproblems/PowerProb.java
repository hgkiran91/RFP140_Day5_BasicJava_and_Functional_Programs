package com.bridgelabz;

public class PowerProb {

    public static void power() {
        int n = 31;
        for (int i = 0; i < n; i++) {
            int value=2*i;
            System.out.println("Power of 2: "+ value);
        }
    }

    public static void main(String[] args) {
        power();
    }
}
